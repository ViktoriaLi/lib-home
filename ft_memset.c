#include "libft.h"

void *ft_memset(void *b, int c, size_t len)
{
	int i;
	unsigned char *d;

	i = 0;
	d = b;
	while(i < len)
	{
		d[i] = c;
		i++;
	}
	b = d;
	return (b);
}

