int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (s1[i] != 0)
		i++;
	while (s2[j] != 0)
		j++;
	if (i != j)
		return (0);
	while (s1[k] != 0)
	{
		if (s1[k] != s2[k])
			return (0);
		k++;
	}
	return (1);
}
